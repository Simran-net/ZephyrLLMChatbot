import gradio as gr
from huggingface_hub import InferenceClient

"""
For more information on `huggingface_hub` Inference API support, please check the docs: https://huggingface.co/docs/huggingface_hub/v0.22.2/en/guides/inference
"""
client = InferenceClient("HuggingFaceH4/zephyr-7b-beta")


def respond(
    message,
    history: list[tuple[str, str]],
    system_message,
    max_tokens,
    temperature,
    top_p,
):
    system_message = "You are a great chef. I advise people how they can enhance the flavour of their food and teach them the tips and tricks that can be used in kitchen while cooking to make the taste better. Discuss your experiences in cooking or let me help you with my knowledge."
    messages = [{"role": "system", "content": system_message}]

    for val in history:
        if val[0]:
            messages.append({"role": "user", "content": val[0]})
        if val[1]:
            messages.append({"role": "assistant", "content": val[1]})

    messages.append({"role": "user", "content": message})

    response = ""

    for message in client.chat_completion(
        messages,
        max_tokens=max_tokens,
        stream=True,
        temperature=temperature,
        top_p=top_p,
    ):
        token = message.choices[0].delta.content

        response += token
        yield response

"""
For information on how to customize the ChatInterface, peruse the gradio docs: https://www.gradio.app/docs/chatinterface
"""
demo = gr.ChatInterface(
    respond,
    additional_inputs=[
        gr.Textbox(value="You are a great chef. I advise people how they can enhance the flavour of their food and teach them the tips and tricks that can be used in kitchen while cooking to make the taste better. Discuss your experiences in cooking or let me help you with my knowledge.", label="System message"),
        gr.Slider(minimum=1, maximum=2048, value=512, step=1, label="Max new tokens"),
        gr.Slider(minimum=0.1, maximum=4.0, value=0.7, step=0.1, label="Temperature"),
        gr.Slider(
            minimum=0.1,
            maximum=1.0,
            value=0.95,
            step=0.05,
            label="Top-p (nucleus sampling)",
        ),
    ],

     examples = [ 
        ["What can I use as a substitute for heavy cream in a pasta sauce?"],
        ["Can you suggest a creative twist on a traditional lasagna recipe?"],
        ["What's a useful tool or gadget every home cook should have?"]
    ],
    title = 'Your Cooking Guide ü•ôüç≥üòã '
)


if __name__ == "__main__":
    demo.launch()
